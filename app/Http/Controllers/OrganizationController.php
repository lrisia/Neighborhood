     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $this->authorize('organization_list', Post::class);
//        $posts = Post::get();
        if (is_null(Auth::user()->organization_id)) {
            $posts = Post::get();
            return redirect()->route('posts.index', [ 'posts' => $posts ]);
        }
        else
            $posts = Post::get()->where('organization_id', Auth::user()->organization_id);
//        dd($posts);
        return view('posts.index', [ 'posts' => $posts ]);
    }

